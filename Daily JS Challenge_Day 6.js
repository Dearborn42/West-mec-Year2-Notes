try{
    const map = fs.readFileSync('Daily JS Challenge_Day 6_Input.txt', 'utf8').split('\r\n')
    let x = 0;
    let y = 0;
    let totalTrees = 0;

    while (y < map.length) {

        if (map[y].charAt(x) === "#") {
            totalTrees++;
            const newString = map[y].substring(0, x) + "X" + map[y].substring(x + 1);
            map[y] = newString;
        }
        if (map[y].charAt(x) === ".") {
            const newString = map[y].substring(0, x) + "O" + map[y].substring(x + 1);
            map[y] = newString;
        }
        
        const right = 3;
        if (x + right < map[y].length) {
            x += right;
        } else {
            const diff = x + right - map[y].length;
            x = diff >= 0 ? diff : 0;
        }
        
        y++;
    }
    console.log("Total trees encountered:", totalTrees);
    console.log(map)
}
